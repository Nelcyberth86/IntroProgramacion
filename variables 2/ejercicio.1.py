#E1: Leer un número por teclado que representa una longitud en millasy mostrar el valor equivalente en kilómetros en pantalla. El mensaje debe tener el siguiente formato
num1_millas = float(input("ingrese, numero en millas: "))
num2_km = (num1_millas * 1.60934)
print(num1_millas,  "equivale a", num2_km, "en kilometros" )
